class Solution
{
    public:
    vector<int> cousins;
    int node_h = -1;
    Node* node_p = nullptr;
    
    bool node_h_p(Node* parent,Node *root,Node * target,int h)
    {
         if(root == nullptr)
           return false;
         if(root == target)
          { 
            node_h = h,node_p = parent;
            return true;
          }
        return node_h_p(root,root->left,target,h+1) || node_h_p(root,root->right,target,h+1);
    }  
    void findcousins(Node* parent,Node* root,Node* target,int h)
    {
        if(root == nullptr)
          return;
        if(node_h == h && parent != node_p)
           cousins.push_back(root->data);
         findcousins(root,root->left,target,h+1);
         findcousins(root,root->right,target,h+1);
    }
    
    vector<int> printCousins(Node* root, Node* node_to_find)
    {
        node_h_p(nullptr,root,node_to_find,0);
        findcousins(nullptr,root,node_to_find,0);
        return cousins.size() ? cousins : vector<int>(1,-1);
    } 
    
};

